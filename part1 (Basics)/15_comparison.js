// Comparison Operators in JavaScript

/*
==   Equal to (checks value only)
===  Strict equal to (checks value and type)
!=   Not equal to (checks value only)
!==  Strict not equal to (checks value and type)
>    Greater than
>=   Greater than or equal to
<    Less than
<=   Less than or equal to
*/

// Equal to (==)
// Compares values only, performs type coercion.
console.log(5 == '5'); // Output: true

// Strict Equal to (===)
// Compares value and type.
console.log(5 === '5'); // Output: false

// Not Equal (!=)
// Compares values only.
console.log(5 != '6'); // Output: true

// Strict Not Equal (!==)
// Compares value and type.
console.log(5 !== '5'); // Output: true

// Greater Than (>)
// Checks if left is greater than right.
console.log(10 > 5); // Output: true

// Greater Than or Equal To (>=)
// Checks if left is greater than or equal to right.
console.log(10 >= 10); // Output: true

// Less Than (<)
// Checks if left is less than right.
console.log(5 < 10); // Output: true

// Less Than or Equal To (<=)
// Checks if left is less than or equal to right.
console.log(5 <= 5); // Output: true
