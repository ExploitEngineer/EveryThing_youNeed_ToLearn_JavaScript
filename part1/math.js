// INFO: Math is JavaScript

// NOTE: Mathematical Operators
/*
1. Addition Operator: +
2. Subtraction Operator: -
3. Multiplication Operator: *
4. Division Operator: /
5. Remainder Operator: %
6. Exponentiation Operator: **
*/

console.log(1 + 1);
console.log(2 - 2);
console.log(2 * 6);
console.log(2 / 6);
console.log(2 % 4);
console.log(2 ** 4);


